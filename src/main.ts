// Initialize the page
function init(): void {
    const comingElement = document.getElementById('ascii-coming');
    const soonElement = document.getElementById('ascii-soon');
    
    if (comingElement) {
        comingElement.textContent = asciiComing;
        setTimeout(() => {
            comingElement.classList.add('loaded');
        }, 100);
    }
    
    if (soonElement) {
        soonElement.textContent = asciiSoon;
        setTimeout(() => {
            soonElement.classList.add('loaded');
        }, 200);
    }
}

// Run on page load
document.addEventListener('DOMContentLoaded', init);
